    serializer_class = AnswerSerializer

    @property
    def campaign(self):
        if not hasattr(self, '_campaign'):
            self._campaign = self.sample.campaign
        return self._campaign

    @property
    def path(self):
        if not hasattr(self, '_path'):
            self._path = self.kwargs.get('path', '')
            if not self._path.startswith('/'):
                self._path = '/' + self._path
        return self._path

    def get_serializer_context(self):
        context = super(SampleAnswersAPIView, self).get_serializer_context()
        context.update({'prefix': self.path})
        return context

    def get_questions(self, prefix):
        return [{
            'path': answer.question.path,
            'rank': answer.rank,
            'title': answer.question.content.title,
            'picture': answer.question.content.picture,
            'extra': self._as_extra_dict(answer.question.content.extra),
        } for answer in self.get_answers(prefix=prefix)]

